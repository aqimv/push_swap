#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t		i;
	char		*ch;

	ch = (char *)b;
	i = 0;
	while (i < len)
	{
		ch[i] = c;
		i++;
	}
	return (b);
}
